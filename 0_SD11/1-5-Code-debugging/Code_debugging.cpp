#include <iostream>

using namespace std;

int main()
{
	int x, y, z;
	
	cout << "Value of x:";
	cin >> x;
	cout << "Value of y:";
	cin >> y;
	cout << "Value of z:";
	cin >> z;
	cout << "x = " << x << ", y = " << y << ", z = " << z << "\n";
	cout << "x + y = " << x + y << '\n';
	cout << "Sum of " << x << " and " << z << " is " << x + z << '\n';
	cout << "y / x = " << y / x << '\n';
	cout << "2 times " << x << " = " << 2 * x << '\n';
}
