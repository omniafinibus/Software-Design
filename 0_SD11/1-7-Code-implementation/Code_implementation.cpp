#include <iostream>

using namespace std;

int main()
{
	int num1, num2, num3;
	float average;
	
	num1 = 125;
	num2 = 28;
	num3 = -25;
	average = (num1 + num2 + num3) / 3.0;

	cout << "   num1 = " << num1 << "\n";
	cout << "   num2 = " << num2 << "\n";
	cout << "   num3 = " << num3 << "\n";
	cout << "Average = " << average << "\n";
}