// functions

/*
	function that adds 2 doubles
	parameters:	a, b entered values which need to be added
	returns:	addition of a and b
*/
double add(double a, double b);

/*
	function that subtracts 2 doubles
	parameters: a, b entered values that need to be subtracted
	returns:	subtraction of a and b
*/
double subtract(double a, double b);

/*
	function that multiplies 2 doubles
	parameters:	a, b entered values that need to be multiplied
	returns:	multiplification of a and b
*/
double multiply(double a, double b);

/*
	function that divides 2 doubles
	parameters:	a, b entered values that need to be divided
	returns:	division of a and b
*/
double divide(double a, double b);