#pragma once

// Function:	Swaps the values of A and B.
// Parameters:	a, b - values chosen by user.
// Returns:		Swapped values of A and B.
void swapVal(int a, int b);

// Function:	Swaps the values of A and B.
// Parameters:	a, b - pointers to the address of the values chosen by user.
// Returns:		Swapped pointers to A and B.
void swapPoint(int* a, int* b);

// Function:	Swaps the values of A and B.
// Parameters:	a, b - addresses of values chosen by user.
// Returns:		Swapped values of A and B.
void swapAddress(int& a, int& b);

// Function:	Swaps the values of A and B.
// Parameters:	a, b - values chosen by user.
// Returns:		Swapped values of A and B.
void swapVal(double a, double b);

// Function:	Swaps the values of A and B.
// Parameters:	a, b - pointers to the address of the values chosen by user.
// Returns:		Swapped pointers to A and B.
void swapPoint(double* a, double* b);

// Function:	Swaps the values of A and B.
// Parameters:	a, b - addresses of values chosen by user.
// Returns:		Swapped values of A and B.
void swapAddress(double& a, double& b);